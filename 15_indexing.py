text = "Ella sabe Python"
TextSize = len(text)
print(TextSize)

print(text)
print(text[5])
print(text[-5])

#Slicing
print(text[0:5])
print(text[:5])
print(text[5:-5])
print(text[::2]) #Desde el inicio al final, saltando cada 02 caracteres. 

